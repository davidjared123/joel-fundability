import { useState, useEffect } from 'react';

export default function LexisNexis({ sectionData, saveData }) {
  const [formData, setFormData] = useState({
    lexisnexis: '',
  });
  const [isEditing, setIsEditing] = useState(true);
  const [showNotification, setShowNotification] = useState(false);

  useEffect(() => {
    if (sectionData) {
      setFormData({
        lexisnexis: sectionData.lexisnexis || '',
      });
      setIsEditing(!sectionData.lexisnexis);
    }
  }, [sectionData]);

  const handleChange = (e) => {
    const { name, value } = e.target;
    setFormData(prev => ({ ...prev, [name]: value }));
  };

  const handleSave = async () => {
    await saveData(formData);
    setIsEditing(false);
    setShowNotification(true);
    setTimeout(() => setShowNotification(false), 2000);
  };

  const handleEdit = () => {
    setIsEditing(true);
  };

  return (
    <div className="p-6 relative">
      {showNotification && (
        <div className="absolute top-0 left-0 right-0 bg-green-500 text-white px-4 py-2 rounded-md flex justify-between items-center">
          <span>Saved successfully!</span>
          <button onClick={() => setShowNotification(false)} className="text-white font-bold">Ã—</button>
        </div>
      )}
      <h3 className="text-2xl font-bold mb-4">LexisNexis</h3>

      <div className="mb-6">
        <p className="text-gray-600 mb-4">
          Do you have your LexisNexis report?
        </p>
        <p className="text-sm text-gray-500 mb-4">
          Many underwriters look at your LexisNexis report to inform their decision. Before applying, know how to pull your LexisNexis report and perform a thorough assessment to best increase your chances of funding.
        </p>
      </div>

      <div className="space-y-4">
        <div className="flex items-center space-x-4">
          <label className="flex items-center">
            <input
              type="radio"
              name="lexisnexis"
              value="Yes"
              checked={formData.lexisnexis === 'Yes'}
              onChange={handleChange}
              disabled={!isEditing}
              className="form-radio h-4 w-4 text-blue-600 transition duration-150 ease-in-out"
            />
            <span className="ml-2 text-sm text-gray-700">Yes</span>
          </label>
          <label className="flex items-center">
            <input
              type="radio"
              name="lexisnexis"
              value="No"
              checked={formData.lexisnexis === 'No'}
              onChange={handleChange}
              disabled={!isEditing}
              className="form-radio h-4 w-4 text-blue-600 transition duration-150 ease-in-out"
            />
            <span className="ml-2 text-sm text-gray-700">No</span>
          </label>
        </div>
        <button
          onClick={isEditing ? handleSave : handleEdit}
          className="bg-blue-600 text-white px-4 py-2 rounded hover:bg-blue-700"
        >
          {isEditing ? 'Save' : 'Edit'}
        </button>
      </div>
    </div>
  );
}